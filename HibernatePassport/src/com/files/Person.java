package com.files;

public class Person {
	int pID;
	String pName;
	Passport pp;
	
	public int getpID() {
		return pID;
	}
	public void setpID(int pID) {
		this.pID = pID;
	}
	public String getpName() {
		return pName;
	}
	public void setpName(String pName) {
		this.pName = pName;
	}
	public Passport getPp() {
		return pp;
	}
	public void setPp(Passport pp) {
		this.pp = pp;
	}
}
